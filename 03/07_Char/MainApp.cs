using System;

namespace _07_Char
// char 형식은 정수를 다루는 데이터 형식 출신이지만 수가 아닌 문자 데이터를 다룬다.
{
    internal class MainApp
    {
        static void Main(string[] args)
        {
            char a = '안';
            char b = '녕';
            char c = '하';
            char d = '세';
            char e = '요';

            Console.Write(a);
            Console.Write(b);
            Console.Write(c);
            Console.Write(d);
            Console.Write(e);
            Console.WriteLine();
        }
    }
}
